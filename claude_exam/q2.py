#Q2. Write a while loop that prints numbers from 5 to 50, counting up in steps of 5 (5, 10, 15, ... 50).
abugu = 5
while 50 >= abugu >= 1:
    if abugu % 5 == 0:
        print(abugu)
    abugu += 1

